weather = 'sunny'
case weather
when 'rainy'
    puts "Make sure you have your umbrella."
when 'windy'
    puts "Hold on to your things!"
when 'sunny'
    puts "Bring out the sunglasses!"
when 'cloudy'
    puts "Dress cozy, the sun will not be out today!"
    when 'snowy'
    puts "You'll need your boots!"
else
    puts "Hmm, I don't know how you should prepare for the this kind of weather!"
end

    